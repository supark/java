/*
Purpose : An object of customer class represents each LinkedList's contents as an object. 
          It contains first name, last name, and balance.  
*/

package bank;

public class customer { //class customer
    String fname, lname;
    double ba;
    
    public customer () { //Constructor
        fname = null;
        lname = null;
        ba = 0.00;
        
    }
    public customer (String f, String l, double d) {  //Constructor with parameters
        fname = f;
        lname = l;
        ba = d;
        
    }
    public String getfname () {  //Function to get firstname
        return fname;
    }
    public String getlname () {  //Function to get lastname
        return lname;
    }
    public double getba() {  //Function to get balance
        return ba;
    }
    public void setfname (String f) {  //Function to set firstname
         fname= f;
    }
    public void setlname (String l) {  //Function to set lastname
        lname= l;
    }
    public void setba (double b) {  //Function to set balance
        ba = b;
    }
}  // end of customer 
